    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int patID= Integer.parseInt(jTextField11.getText());
        String patname=crsystem.getPatient(patID).getName();
        String hospname= (String)jComboBox4.getSelectedItem();
        int hospID= crsystem.getHospital(hospname).getId();
        Hospital hosp= crsystem.getHospital(hospID);
        
        String sectname= (String)jComboBox5.getSelectedItem();
        int sectID=hosp.getSection(sectname).getId();
        Section sect=hosp.getSection(sectID);
        
        String docname= (String)jComboBox6.getSelectedItem();
        int docID=sect.getDoctor(docname).getDiplomaId();
        
        int day= jComboBox10.getSelectedIndex()+1;
        int month= jComboBox11.getSelectedIndex()+1;
        int year=jComboBox12.getSelectedIndex()+1980;
        

        Date desiredDate= new Date(year,month,day);

        
        try{
            boolean success= crsystem.makeRendezvous(hospID, patID, sectID, docID, desiredDate);
            
            if(success){
                jTextArea5.append(day+"/"+month+"/"+year+"\t"+patID+"\t"+patname+"\t\t"+docname+"\n");
            }
            
           
            
        }catch(IDException e){
            e.printStackTrace();
        }

        
    }//GEN-LAST:event_jButton5ActionPerformed

