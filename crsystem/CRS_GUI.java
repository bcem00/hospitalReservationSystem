    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        jTextArea6.setText("Date\tPatient Name\t\tDoctor Name\t\tHospital\tSection\n");
        String docname= (String)jComboBox7.getSelectedItem();
        String sectname= (String)jComboBox8.getSelectedItem();
        String hospname=(String)jComboBox9.getSelectedItem();
        
        Hospital hosp= crsystem.getHospital(hospname);
        Section sect= hosp.getSection(sectname);
        Doctor doc= sect.getDoctor(docname);
        
        jTextArea6.setText("Date\tPatient Name/Patient ID\tDoctor Name\tHospital\tSection\n");
        for(Rendezvous r: doc.getSchedule().getSessions()){
            jTextArea6.append(r.toString()+"\t"+ hospname+"\t"+sectname+"\n");
        }
        
        
        
        
       
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jComboBox8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox8ActionPerformed
        // TODO add your handling code here:
        String hospname= (String)jComboBox9.getSelectedItem();
        Hospital hosp= crsystem.getHospital(hospname);
        String sectname= (String)jComboBox8.getSelectedItem();
        Section sect= hosp.getSection(sectname);
        
        jComboBox7.removeAllItems();
        for(Doctor d: sect.getDoctors()){
            jComboBox7.addItem(d.getName());
        }
    }//GEN-LAST:event_jComboBox8ActionPerformed

