    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        try {
            // TODO add your handling code here:
            crsystem.loadFromFile("clinic_data.ser");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CRS_GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        if(crsystem != null){
            jComboBox2.removeAllItems();
            jComboBox4.removeAllItems();
            jComboBox1.removeAllItems();
            jComboBox3.removeAllItems();
            jComboBox5.removeAllItems();
            jComboBox6.removeAllItems();
            jComboBox7.removeAllItems();
            jComboBox8.removeAllItems();
            jComboBox9.removeAllItems();
            jTextArea1.setText("");
            jTextArea1.append("ID\tName\t\tNo. of Sections\n");
            jTextArea1.append(crsystem.listHospitals());
            for(Hospital h: crsystem.getHospitals().values()){
                jComboBox1.addItem(h.getName());
                jComboBox2.addItem(h.getName());
                jComboBox4.addItem(h.getName());
                jComboBox9.addItem(h.getName());
                for(Section s: h.getSections()){
                    jComboBox3.addItem(s.getName());
                    jComboBox5.addItem(s.getName());
                }
            }
            
            
   
            
            jTextArea3.setText("");
            jTextArea3.append("Name\tID\n");
            for(Patient p: crsystem.getPatients().values()){
                jTextArea3.append(p.toString()+ " \n");
            }
            
            jTextArea4.setText("");
            jTextArea4.append("Doctor Name\tNat.ID\tDip.ID\tHospital\tSection\n");
            
            for(Hospital h : crsystem.getHospitals().values()){
                for(Section s: h.getSections()){
                    for(Doctor d: s.getDoctors()){
                        jTextArea4.append(d.toString()+"\t"+s.getName()+"\t"+h.getName()+"\n");
                    }   
                }
            }
            
            jTextArea5.setText("Date\tPatient ID\tPatient Name\tDoctor Name\n");
            
            for(Rendezvous r: crsystem.getRendezvous()){
                jTextArea5.append(r.toString());
            }
            
            
        }
        
    }//GEN-LAST:event_jMenuItem2ActionPerformed

